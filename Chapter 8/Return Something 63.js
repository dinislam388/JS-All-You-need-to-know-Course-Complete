// Return a Object 

function returnSomething(name, email) {
    return {
        name: name,
        email: email
    }
}
const result = returnSomething('MD Din Islam', 'dinislam388415@gmail.com')
console.log(result);


// Return A array in function 

function arrayReturnSomething(a, b, c, d ) {
    return [
        a = a,
        b = b,
        c = c,
        d = d
    ]
}
const resultArray = arrayReturnSomething(5, 10, 15, 20)
console.log(resultArray);
